"""  Arithmetic Operators  """
a=float(input("Enter the value of a:"))
b=float(input("Enter the value of b:"))
a,b=[int(X) for X in input ('Enter the value of a')]
print(f"The value of {a} + {b} is {a+b}")
print(f"The value of {a} - {b} is {a-b}")
print(f"The value of {a} * {b} is {a*b}")
print(f"The value of {a} / {b} is {a/b}")
print(f"The value of {a} % {b} is {a%b}")
print(f"The value of {a} ** {b} is {a**b}")
